package com.kodilla.hibernate.manytomany.dao;

import com.kodilla.hibernate.manytomany.Company;
import com.kodilla.hibernate.manytomany.Employee;
import com.kodilla.hibernate.task.Task;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;

import java.util.List;

import static org.junit.jupiter.api.Assertions.*;

@SpringBootTest
public class CompanyDaoTestSuite {

    @Autowired
    private CompanyDao companyDao;

    private EmployeeDao employeeDao;

    @Test
    void testSaveManyToMany() {
        //Given
        Employee johnSmith = new Employee("John", "Smith");
        Employee stephanieClarckson = new Employee("Stephanie", "Clarckson");
        Employee lindaKovalsky = new Employee("Linda", "Kovalsky");

        Company softwareMachine = new Company("Software Machine");
        Company dataMaesters = new Company("Data Maesters");
        Company greyMatter = new Company("Grey Matter");

        softwareMachine.getEmployees().add(johnSmith);
        dataMaesters.getEmployees().add(stephanieClarckson);
        dataMaesters.getEmployees().add(lindaKovalsky);
        greyMatter.getEmployees().add(johnSmith);
        greyMatter.getEmployees().add(lindaKovalsky);

        johnSmith.getCompanies().add(softwareMachine);
        johnSmith.getCompanies().add(greyMatter);
        stephanieClarckson.getCompanies().add(dataMaesters);
        lindaKovalsky.getCompanies().add(dataMaesters);
        lindaKovalsky.getCompanies().add(greyMatter);

        //When
        companyDao.save(softwareMachine);
        int softwareMachineId = softwareMachine.getId();
        companyDao.save(dataMaesters);
        int dataMaestersId = dataMaesters.getId();
        companyDao.save(greyMatter);
        int greyMatterId = greyMatter.getId();

        //Then
        assertNotEquals(0, softwareMachineId);
        assertNotEquals(0, dataMaestersId);
        assertNotEquals(0, greyMatterId);

        //CleanUp
        try {
            companyDao.deleteById(softwareMachineId);
            companyDao.deleteById(dataMaestersId);
            companyDao.deleteById(greyMatterId);
        } catch (Exception e) {
            //do nothing
        }
    }

    @Test
    void testNamedQueries() {
        Employee johnSmith = new Employee("John", "Smith");
        Employee stephanieClarckson = new Employee("Stephanie", "Clarckson");
        Employee lindaKovalsky = new Employee("Linda", "Kovalsky");

        Company softwareMachine = new Company("Software Machine");
        Company dataMaesters = new Company("Data Maesters");
        Company greyMatter = new Company("Grey Matter");

        softwareMachine.getEmployees().add(johnSmith);
        dataMaesters.getEmployees().add(stephanieClarckson);
        dataMaesters.getEmployees().add(lindaKovalsky);
        greyMatter.getEmployees().add(johnSmith);
        greyMatter.getEmployees().add(lindaKovalsky);

        johnSmith.getCompanies().add(softwareMachine);
        johnSmith.getCompanies().add(greyMatter);
        stephanieClarckson.getCompanies().add(dataMaesters);
        lindaKovalsky.getCompanies().add(dataMaesters);
        lindaKovalsky.getCompanies().add(greyMatter);

        //When
        companyDao.retrieveCompany(johnSmith.getFirstname());
        companyDao.retrieveCompany(stephanieClarckson.getFirstname());
        companyDao.retrieveCompany(lindaKovalsky.getFirstname());
        employeeDao.retrieveEmployee(johnSmith.getFirstname());
        employeeDao.retrieveEmployee(stephanieClarckson.getFirstname());
        employeeDao.retrieveEmployee(lindaKovalsky.getFirstname());


        List<Company> retrieceCompany1 = companyDao.retrieveCompany(johnSmith.getFirstname());
        List<Company> retrieceCompany2 = companyDao.retrieveCompany(stephanieClarckson.getFirstname());
        List<Company> retrieceCompany3 = companyDao.retrieveCompany(lindaKovalsky.getFirstname());

        List<Employee> retrieceEmployee1 = employeeDao.retrieveEmployee(lindaKovalsky.getFirstname());
        List<Employee> retrieceEmployee2 = employeeDao.retrieveEmployee(lindaKovalsky.getFirstname());
        List<Employee> retrieceEmployee3 = employeeDao.retrieveEmployee(lindaKovalsky.getFirstname());

        //Then
        try {
            assertEquals(1, retrieceCompany1.size());
            assertEquals(3, retrieceCompany2.size());
            assertEquals(3, retrieceCompany3.size());
            assertEquals(2, retrieceEmployee1.size());
            assertEquals(2, retrieceEmployee2.size());
            assertEquals(2, retrieceEmployee3.size());
        } finally {
            //Cleanup

        }

    }
}
